<template>
    <div class="card border-info">
        <!--<br>-->
        <h3 class="card-header bg-transparent border-info">个人信息 <a href="javascript:window.location.reload();" class="btn btn-outline-info"><em class="fa fa-refresh"> 刷新</em></a></h3>
        <div class="card-body">
            姓名:<h4 class="label" v-text="this.person.name"></h4> <br>
            电话: <h4 class="label" v-text="this.person.mobile"></h4> <br>
            邮箱:<h4 class="label" v-text="this.person.email"></h4> <br>
        </div>
    </div>
</template>

<script>
    export default{

        data(){
            return {
                person: {
                    name: '',
                    mobile: '',
                    email: ''
                }
            }
        },

        created() {
            this.person = this.loadPerson();
        },

        methods:{

            loadPerson(){
                return axios.get('/user/profile').then(response => {
                    this.person = response.data;
                }).catch(error => {
                    console.log(error)
                })
            }
        }
    }
</script>